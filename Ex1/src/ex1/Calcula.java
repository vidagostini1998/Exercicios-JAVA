public class Calcula {
    public static void soma(double n1,double n2){
        double res = n1+n2;
        System.out.println("A soma dos numeros é igual a: "+res);
    }
    
    public static void subtracao(double n1,double n2){
        double res = n1-n2;
        System.out.println("A diferença dos numeros é igual a: "+res);
    }
    
    public static void multiplicacao(double n1,double n2){
        double res = n1*n2;
        System.out.println("A multiplicação dos numeros é igual a: "+res);
    }
    
    public static void divisao(double n1,double n2){
        double res = n1/n2;
        System.out.println("A divisão dos numeros é igual a: "+res);
    }
}
